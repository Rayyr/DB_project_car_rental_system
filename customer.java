    private void addcustbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addcustbtnActionPerformed
        // add new customer(register)

        String fname = this.jtf_fn.getText();
        String lname = this.jtf_ln.getText();
        String email = this.jtf_email.getText();
        String phone = this.jtf_phone.getText();
        String password = this.jtfp_password.getText();

        String arr[] = {fname, lname, email, phone, password};

        for (int c = 0; c < 5; c++) {
            if (verify_strings(arr[c]) == true) {
                JOptionPane.showMessageDialog(null, "Invalid Input Values !", "Spaces Errors ", JOptionPane.OK_OPTION + JOptionPane.ERROR_MESSAGE);
                this.reinitialize_the_feilds();
                return;

            }

        }

        //when the compiler reaches this line all values are accepted and correct
        make_connector();
        try {
            con.setAutoCommit(false);
            String q = "insert into customer values('" + lastind + "','" + fname + "','" + lname + "','" + phone + "','" + email + "','" + password + "')";
            Statement s = con.createStatement();
            s.executeUpdate(q);
            //increament the id for the next id brand
            lastind++;
            this.jtf_id.setText(String.valueOf(lastind));
            con.commit();
            con.close();
            con = null;

            JOptionPane.showMessageDialog(null, "New Customer Has Been Successfully Added To Your Comapny !", "Customer Added", JOptionPane.OK_OPTION + JOptionPane.PLAIN_MESSAGE);
            reinitialize_the_feilds();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Error Happens With Your Data Base Connection!", "Data Base Error", JOptionPane.ERROR_MESSAGE + JOptionPane.OK_OPTION);
            this.dispose();
        }


    }//GEN-LAST:event_addcustbtnActionPerformed

    public boolean verify_strings(String x) {

        if ((x.contains(" ") == true) || (x.isEmpty() == true)) {
            return true;//has spaces
        }
        return false;// not has spaces

    }

    public void reinitialize_the_feilds() {

        this.jtf_email.setText(null);
        this.jtf_fn.setText(null);
        this.jtf_ln.setText(null);
        this.jtf_phone.setText(null);
        this.jtfp_password.setText(null);

        return;
    }

